    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
        int id = Integer.parseInt(JOptionPane.showInputDialog(this, "Enter Teller ID to Edit:"));
        String name = JOptionPane.showInputDialog(this, "Enter New Name:");
        int serviceTypeId = Integer.parseInt(JOptionPane.showInputDialog(this,
            "Enter New Service Type ID:\n1 = Deposit\n2 = Withdrawal\n3 = Loan Inquiry\n4 = Account Opening"));

        if (name != null) {
            manager.updateTeller(id, name, serviceTypeId);
            loadTellersToTable();
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid input.");
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
          if (tellerTable.getRowCount() >= 5) {
        JOptionPane.showMessageDialog(this, "You can only add up to 5 tellers.");
        return;
    }

    String firstName = JOptionPane.showInputDialog(this, "Enter Teller's First Name:");
    String lastName = JOptionPane.showInputDialog(this, "Enter Teller's Last Name:");
    
    String input = JOptionPane.showInputDialog(this,
        "Enter Service Type ID:\n1 = Deposit\n2 = Bills Payment\n3 = Withdrawal\n4 = Loan Inquiry\n5 = Account Opening");

    if (firstName != null && lastName != null && input != null) {
        try {
            int serviceTypeId = Integer.parseInt(input);
            manager.addTeller(firstName, lastName, serviceTypeId);
            loadTellersToTable();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid service type ID.");
        }
      }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
        int id = Integer.parseInt(JOptionPane.showInputDialog(this, "Enter Teller ID to Delete:"));
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            manager.deleteTeller(id);
            loadTellersToTable();
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid ID.");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

